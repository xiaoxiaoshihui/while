#include <stdio.h>

int main() {
	
	int line = 0;
	//循环
	while (line < 30000) {
		printf("写代码\n");
		printf("第%d行代码\n", line);
		line++;
	}
	if (line == 30000) {

		printf("写了3W行代码了，666!");
	}
	return 0;
}

//有些事情必须一直做，比如日复一日的学习，日复一日的吃饭，地球日复一日的自转，日复一日的绕着太阳公转
//就可以用到while语句 while()里写条件表达式，如果满足就一直运行{}里面的程序如果不满足就运行{}里面的程序
//如果while(1){}则是死循环，程序将一直运行{}里的程序，日复一日直到永远